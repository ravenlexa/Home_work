#  Когда используется критерий Стьюдента, а когда Z –критерий?

# Критерий Стьюдента используется когда неизвестна дисперсия генеральной совокупности и размер выборки небольшой (n < 30).
#
# Z - критерий используется когда размер выборки большой (n > 30) и дисперсия генеральной совокупности известна.
